def sum(a, b):
    """
    This function adds two numbers.
    :param a:
    :param b:
    :return: sum of the two numbers
    """

    return a + b
 
def subtraction(a, b):
    """
    This function subtracts second number from the first.
    :param a:
    :param b:
    :return: difference of the two numbers
    """

    return a - b

